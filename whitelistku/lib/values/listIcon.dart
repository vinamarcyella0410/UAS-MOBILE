class IconAttrb {
  final int iconType;
  final String iconValue;

  IconAttrb(this.iconType, this.iconValue);

  get icon {
    switch (iconType) {
      case 0:
        return MaterialIcons.getIconData(iconValue);
        break;
      case 1:
        return Ionicons.getIconData(iconValue);
        break;
      case 2:
        return AntDesign.getIconData(iconValue);
        break;
      case 3:
        return FontAwesome.getIconData(iconValue);
        break;
      default:
        break;
